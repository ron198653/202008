public class Test01 {
    /*
     *循环的维度：循环嵌套
     * 一维循环：一条线
     * 二维循环：平面
     * 三维循环：立体空间
     * 四维循环：立体空间+时间
     */
    public static void main(String[] args) {
        for(int i = 0;i<=5;i++){
            System.out.println('*');
        }
        System.out.println("--------------------------------------------");
        for(int i = 0;i <=5; i++){
            System.out.print('*');
        }
    }
}

